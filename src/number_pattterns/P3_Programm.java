package number_pattterns;

public class P3_Programm {
    public static void main(String[] args) {
        for(int i=1;i<=5;i++) //5<=5
        {
            for(int j=i;j<=5;j++) //5<=5
            {
                System.out.print(j); //5
            }
            System.out.println();
        }
    }
    
}
