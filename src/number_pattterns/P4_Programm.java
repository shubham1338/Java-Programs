package number_pattterns;

public class P4_Programm {
    public static void main(String[] args) {
        for(int i=1;i<=5;i++) //2<=5
        {
            for(int j=5;j>=1;j--) //4>=1
            {
                System.out.print(j); //1
            }
            System.out.println();
        }
    }
    
}
